	MOVF X,W
	BTFSC Y,0
	ADDWF RESU, F
	RRF Y,F
	BCF STATUS,C
	RLF X,F
	MOVF CONT,W
	XORLW .4
	BTFSS STATUS, Z
	GOTO MULT
	NOP
